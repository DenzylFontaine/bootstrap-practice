					Nasio is the youngest of seven children born to a Carib Indian mother and father of African descent, in the village of Carte-Bois. When he was eight years old, Nasio began singing in the Bagatelle school and the village Catholic church choirs. Nasio had a habit of making instruments out of everything he could find. His father, Atto, made his very first instrument, a piece of board with fishing lines for strings. He was inspired at an early age by calypso artists and soul musicians such as Curtis Mayfield, Sam Cooke, and Marvin Gaye. In 1981, he moved from Dominica to the island of St. Maarten where he became influenced by Rastafari and reggae artists such as Joseph Hill, Burning Spear, Jacob Miller, and Bob Marley, to whom he is often compared. He recorded his first 12-inch single "Born to Be Free" in 1986, which sold over 5,000 copies in St. Maarten (the island's biggest ever selling single), and followed it with the EP Babylon is Falling in 1990. His first full-length CD Reggae Power (1994) was followed by Wolf Catcher (1997) and Revolution (1999). Living in the Positive was released by Ras Records in 2004. In 2005 Nasio recorded the album Universal Cry at Long View in Massachusetts and Peter Gabriel's Real World Studios in the UK; collaborating with producer Richard Evans. Universal Cry was released on the Greensleeves record label in the summer of 2006 to critical acclaim. Fontaine's popularity has spread to Africa, where he performed in 2006. He also performed at the Glastonbury Festival in the UK in 2007.A compilation of material from his earlier albums, Rise Up, was released in 2007.
